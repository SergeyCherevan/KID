namespace KID
{
    /// <summary>
    /// Статус клика мыши на Canvas.
    /// </summary>
    public enum ClickStatus
    {
        /// <summary>
        /// Нет клика.
        /// </summary>
        NoClick,
        
        /// <summary>
        /// Одиночный клик левой кнопкой мыши.
        /// </summary>
        OneLeftClick,
        
        /// <summary>
        /// Одиночный клик правой кнопкой мыши.
        /// </summary>
        OneRightClick,
        
        /// <summary>
        /// Двойной клик левой кнопкой мыши.
        /// </summary>
        DoubleLeftClick,
        
        /// <summary>
        /// Двойной клик правой кнопкой мыши.
        /// </summary>
        DoubleRightClick
    }
}
